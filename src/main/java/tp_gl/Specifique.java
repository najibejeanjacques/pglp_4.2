package tp_gl;

public class Specifique {

	/**
	 * 
	 * @param a premiere valeur 
	 * @param b deuxieme valeur
	 * @return retourne l'addition des deux valeurs
	 */
	public double plus(double a, double b)
	{
		return a+b;
	}

	/**
	 * 
	 * @param a premiere valeur 
	 * @param b deuxieme valeur
	 * @return retourne la soustraction des deux valeurs
	 */
	public double moins(double a, double b)
	{
		return a-b;
	}
	

	/**
	 * 
	 * @param a premiere valeur 
	 * @param b deuxieme valeur
	 * @return retourne la multiplicationdes deux valeurs
	 */
	public double multi(double a, double b)
	{
		return a*b;
	}
	

	/**
	 * 
	 * @param a premiere valeur 
	 * @param b deuxieme valeur
	 * @return retourne la division des deux valeurs
	 */
	public double div(double a, double b)
	{
		return a/b;
	}
}
